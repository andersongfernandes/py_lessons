#type casting = convert the data type of a value to another data type

# to string
x = 1 #int
y = 2.0 #float
z = "3" #str

x = str(x)
y = str(y)
z = str(z)

print("converting to string")
print(x)
print(y)
print(2*z)

# to float
x = 1 #int
y = 2.0 #float
z = "3" #str

x = float(x)
y = float(y)
z = float(z)

print("converting to float")
print(x)
print(y)
print(2*z)

# to integer
x = 1 #int
y = 2.0 #float
z = "3" #str

x = int(x)
y = int(y)
z = int(z)

print("converting to integer")
print(x)
print(y)
print(2*z)